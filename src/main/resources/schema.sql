CREATE TABLE CUSTOMER (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR(255)
);

CREATE TABLE RATE (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CUSTOMER_ID BIGINT,
    RATE DOUBLE,
    FROM_DATE DATE,
    TO_DATE DATE
);

CREATE TABLE PROJECT (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TITLE VARCHAR(255),
    SUB_TITLE VARCHAR(255),
    CUSTOMER_ID BIGINT,
    ACTIVE BOOLEAN,
    BILLABLE BOOLEAN,
    PRIORITY INTEGER
);

CREATE TABLE BOOKING (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PROJECT_ID BIGINT,
    FROM_DATE TIMESTAMP,
    TO_DATE TIMESTAMP,
    MINUTES INTEGER,
    BREAK_MINUTES INTEGER,
    BILLABLE BOOLEAN,
    NOTE VARCHAR(255)
);
